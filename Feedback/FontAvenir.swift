import Foundation
import UIKit

extension UIFont {
    
    static func avenirUltraLight(fontSize size: CGFloat) -> UIFont{
        return UIFont(name: "AvenirNext-UltraLight", size: size)!
    }
    static func avenirMedium(fontSize size: CGFloat) -> UIFont{
        return UIFont(name: "AvenirNext-Medium", size: size)!
    }
    static func avenirBold(fontSize size: CGFloat) -> UIFont{
        return UIFont(name: "AvenirNext-Bold", size: size)!
    }
}